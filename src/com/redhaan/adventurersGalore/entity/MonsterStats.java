package com.redhaan.adventurersGalore.entity;

public class MonsterStats {
	
	private int HP;
	private int MP;
	private int PHY;
	private int AGI;
	private int MAG;
	
	public MonsterStats() {
		
	}

	public int getHP() {
		return HP;
	}

	public void setHP(int hP) {
		HP = hP;
	}

	public int getMP() {
		return MP;
	}

	public void setMP(int mP) {
		MP = mP;
	}

	public int getPHY() {
		return PHY;
	}

	public void setPHY(int pHY) {
		PHY = pHY;
	}

	public int getAGI() {
		return AGI;
	}

	public void setAGI(int aGI) {
		AGI = aGI;
	}

	public int getMAG() {
		return MAG;
	}

	public void setMAG(int mAG) {
		MAG = mAG;
	}

}
